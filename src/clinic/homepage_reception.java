    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
// TODO add your handling code here:\
                        try
       {
         Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
         con=DriverManager.getConnection("jdbc:odbc:rock");
         Statement st=con.createStatement();  
         ResultSet rs=st.executeQuery("select convert(varchar(12),getdate(),106) as Dates");        
         while(rs.next())
          {
             String a;
             a=rs.getString("Dates");
             lbldate.setText(a);
           }
        }
       catch(SQLException sq)
         {
            JOptionPane.showMessageDialog(this,"Database Error","Time",JOptionPane.ERROR_MESSAGE);
            this.dispose();
         } 
               catch(Exception e)
         {
         JOptionPane.showMessageDialog(this,"Error"+e);
          this.dispose();
         }
       
        
        
    }//GEN-LAST:event_formWindowActivated

