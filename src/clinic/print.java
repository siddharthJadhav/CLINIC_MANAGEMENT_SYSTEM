    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
// TODO add your handling code here:
//       lblname.setVisible(false);
        lblvalue.setVisible(false);
        String name,value;
        name=lblname.getText();
        value=lblvalue.getText();
        
      int i;
      ResultSet rs; 
      Vector columnnames=new Vector();
      Vector data=new Vector();
   
      try
       {
          Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
          Connection con=DriverManager.getConnection("jdbc:odbc:rock");
          Statement st=con.createStatement();
          
          if(name.equals("Diesease"))
          {
              lblname.setText("DIESEASE");
            rs=st.executeQuery("select distinct name,code from treatement where diesease like '%"+value+"%'");
          }
          
          else if(name.equals("Allergy"))
          {
              lblname.setText("ALLERGY");
             rs=st.executeQuery("select fname,mname,lname,code,occupation,address from register where allergy like '%"+value+"%'");        
          }
          
          else if(name.equals("Address"))
          {
              lblname.setText("ADDRESS");
          rs=st.executeQuery("select fname,mname,lname,code,occupation,address from register where address like '%"+value+"%'");
          }
              
          else if(name.equals("Code"))
          {
              lblname.setText("FAMILY");
          rs=st.executeQuery("select fname,mname,lname,code,reference,relation from register where reference like '%"+value+"%' or code like  '%"+value+"%'");
          }
          
           else if(name.equals("<")||name.equals(">")||name.equals("="))
           {
              lblname.setText("AGE");
            rs=st.executeQuery("select fname,code,gender,age,address from register where age "+name+" "+value+"");
           }
           else
           { lblname.setText("AGE");
               rs=st.executeQuery("select fname,code,gender,age,address from register where age between '"+value+"' "+name+"");
           }
         ResultSetMetaData md=rs.getMetaData();
         int columns=md.getColumnCount();
         for(i=1;i<=columns;i++)
          {
             columnnames.addElement(md.getColumnName(i));
          }
         while(rs.next())
          {
             Vector row=new Vector(columns);
             for(i=1;i<=columns;i++)
               {
                 row.addElement(rs.getObject(i));
               }
             data.addElement(row);
          }
                 
         st.close();
         con.close();
      }        
       catch(SQLException sq)
         {
            JOptionPane.showMessageDialog(this,"Database Error","Register",JOptionPane.ERROR_MESSAGE);
            this.dispose();
         } 
               catch(Exception e)
         {
         JOptionPane.showMessageDialog(this,"Error"+e);
          this.dispose();
         }

      JTable jTable4=new JTable(data,columnnames);
      JScrollPane jScrollPane4=new JScrollPane(jTable4);
      jTable1.setCellEditor(jTable4.getCellEditor());
      jScrollPane1.setViewportView(jTable4);
        
        
    }//GEN-LAST:event_formWindowActivated
    
